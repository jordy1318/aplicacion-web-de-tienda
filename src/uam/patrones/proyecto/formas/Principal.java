public void agregarLinea(String a){
System.out.println("aqui va el nombre: "+a);
 try {
            
          
            
            PreparedStatement pps2 = cn.prepareStatement("insert into LineaVenta(cantidad, Producto_idProducto) values(?,?)");
            
            
            pps2.setString(1, txtCantidad.getText());
            pps2.setString(2, a);
            pps2.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos linea guardados");
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
}

public void imprimeLista(List<LineaDeVenta> lista){
    String listaCompleta= "";
    Iterator iter = lista.iterator();
    
    while(iter.hasNext()){
        LineaDeVenta linea = (LineaDeVenta) iter.next();
        listaCompleta = listaCompleta +"\n"+linea.toString();
        
    }
    
    txtResultados.setText(listaCompleta);
    
    }

